# For Loops:
for i in range(11):
    print(i)
for i in range(50, -1, -1):
    print(i)
for i in range(200, 501, 2):
    print(i)
for i in range(1, 51, 2):
    print(i)
surname = input("Please enter your surname: ")
for i in surname:
    print(i)
