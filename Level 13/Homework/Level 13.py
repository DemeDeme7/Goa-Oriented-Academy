# While Loops:
num = 10
while num <= 15:
    print(num)
    num += 1
num = 50
while num >= 0:
    print(num)
    num -= 1
# While Loop-ები გამოიყენება კოდის გასამეორებლად,მანამდე სანამ კოდის/ხაზის აზრი არის True.Counter ცვლადი გამოიყენება იმის დასათვლელად,თუ რამდენჯერ
# არის განმეორება კოდში,ეს არის Loop-ის გზა რომ დაითვალოს თითოეული იტერაცია.
